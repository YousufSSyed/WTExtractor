#include "WTVector3D.hpp"

float WTVector3D::getY(){
  return this->y;
}

void WTVector3D::setY(float y){
  this->y = y;
}

float WTVector3D::getZ(){
  return this->z;
}

void WTVector3D::setZ(float z){
  this->z = z;
}

float WTVector3D::getX(){
  return this->x;
}

void WTVector3D::setX(float x){
  this->x = x;
}
